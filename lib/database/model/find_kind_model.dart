
class FindKindModel {

  String group = "";

  String origin = "";

  String kindName = "";

  String kindUrl = "";

  FindKindModel();

  String getGroup() => group;
  void setGroup(String group) => this.group = group;

  String getOrigin() => origin;
  void setOrigin(String origin) => this.origin = origin;

  String getKindName() => kindName;
  void setKindName(String kindName) => this.kindName = kindName;

  String getKindUrl() => kindUrl;
  void setKindUrl(String kindUrl) => this.kindUrl = kindUrl;
}
